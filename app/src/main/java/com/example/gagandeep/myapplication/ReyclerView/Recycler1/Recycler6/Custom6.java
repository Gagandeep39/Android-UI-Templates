public class Custom6 {

    private String head;
    private String body;
    private int resourceId;
    public Custom6(String head, String body, int resourceId) {
        this.head = head;
        this.body = body;
        this.resourceId = resourceId;
    }
    public String getHead(){
        return head;
    }
    public String getBody(){
        return body;
    }
    public int getResourceId(){
        return resourceId;
    }


}
